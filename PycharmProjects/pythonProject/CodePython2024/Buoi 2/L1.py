#Câu 1: Tính tổng, hiệu, tích và thương của hai số a và b, với a và b nhập từ bàn phím.
a = int(input("Nhap So a: "))
b = int(input("Nhap So b: "))

cong_so = a + b
tru_so = a - b
nhan_so = a * b
chia_so = a / b
print("Tổng là", cong_so)
print("Hiệu là",tru_so)
print("Tích là",nhan_so)
print("Thương là",chia_so)

